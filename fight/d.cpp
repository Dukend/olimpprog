#include <bits/stdc++.h>
using namespace std;

int main()
{
	long long n, a, b;
	cin >> n;
	for (int i = 0; i < n; i++)
	{
		cin >> a >> b;

		if (((a - b) % 2 == 1) || ((b - 1) % 2 == 1))
			cout << "Alice";
		else
			cout << "Bob";
		cout << endl;
	}

	return 0;
}
