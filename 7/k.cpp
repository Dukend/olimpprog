#include <bits/stdc++.h>
using namespace std;
typedef long long ll;
struct len
{
	ll l, r;
};
bool qq(len &lhs, len &rhs)
{
	if (lhs.r != rhs.r)
		return (lhs.r < rhs.r);
	else
		return (lhs.l < rhs.l);
}
int main()
{
	ll n;
	cin >> n;
	vector<len> v(n);
	for (int i = 0; i < n; ++i)
		cin >> v[i].l >> v[i].r;
	sort(v.begin(), v.end(), qq);
	ll rr = v[0].l, ans = 0;
	for (int i = 0; i < n; ++i)
	{
		if (rr <= v[i].l)
		{
			ans++;
			rr = v[i].r;
		}
	}
	cout << ans;
	return 0;
}
