#include <bits/stdc++.h>
using namespace std;
int main()
{
	int n, m, l, r, ans = 0;
	cin >> n >> m >> l >> r;
	ans = r - l - 1;
	for (int i = 1; i < n; i++)
	{
		cin >> l >> r;
		ans ^= r - l - 1;
	}
	if (ans)
		cout << 1;
	else
		cout << 2;
	return 0;
}
